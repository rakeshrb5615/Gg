package com.google.android.gms.auth;
public class GoogleAuthException extends java.lang.Exception {

    public GoogleAuthException()
    {
        return;
    }

    public GoogleAuthException(String p1)
    {
        super(p1);
        return;
    }

    public GoogleAuthException(String p1, Throwable p2)
    {
        super(p1, p2);
        return;
    }

    public GoogleAuthException(Throwable p1)
    {
        super(p1);
        return;
    }
}
