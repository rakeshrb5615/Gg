package com.pairip.licensecheck;
public final class LicenseCheckException extends java.lang.Exception {

    public LicenseCheckException(String p1)
    {
        super(p1);
        return;
    }

    public LicenseCheckException(String p1, Throwable p2)
    {
        super(p1, p2);
        return;
    }
}
